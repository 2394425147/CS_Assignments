name = "Xingkai Chen"
uppercased_name = name.upper()
lowercased_name = name.lower()
titlecased_name = name.title()

print(uppercased_name)
print(lowercased_name)
print(titlecased_name)

assert uppercased_name == "XINGKAI CHEN"
assert lowercased_name == "xingkai chen"
assert titlecased_name == "Xingkai Chen"
